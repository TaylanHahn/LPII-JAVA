/* Exemplo para demonstrar a estrutura de repetição while.
 */
public class TestaWhile1 {
  
  public static void main(String[] args){
    int c;
    
    //Exemplo de laco "while".
    c = 1;
    while (c<=10) {
      System.out.println("c = " + c);
      c++;
    }
    System.out.println("Ultimo = " + c);
    System.out.println();
    
  }//main()  
}//TestaWhile1.