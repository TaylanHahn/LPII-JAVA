/* Exemplo para demonstrar a utilização da estrutura de repetição 
 * for especial "for each" (para cada), para acessar os elementos de um array.
 */
public class Array3{
  
  public static void main(String[] args){
    
    float[] v = {100, 200, 300, 400, 500};
    
    for (float f: v) {
      System.out.println(f);
    }
    
  }//main()
  
}//Array3.